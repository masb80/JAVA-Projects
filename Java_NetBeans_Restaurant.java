    private void jBtnEqualsActionPerformed(java.awt.event.ActionEvent evt) {                                           
        String answer;
        secondnum = Double.parseDouble(jtxtDisplay.getText());
        if (operations == "+")
        {
            result = firstnum +  secondnum;
            answer = String.format("%.2f",result);
            jtxtDisplay.setText(answer);
        }
        else  if (operations == "-")
        {
            result = firstnum -  secondnum;
            answer = String.format("%.2f",result);
            jtxtDisplay.setText(answer);
        }
        else  if (operations == "*")
        {
            result = firstnum *  secondnum;
            answer = String.format("%.2f",result);
            jtxtDisplay.setText(answer);
        }
        else  if (operations == "/")
        {
            result = firstnum /  secondnum;
            answer = String.format("%.2f",result);
            jtxtDisplay.setText(answer);

        }
        else  if (operations == "%")
        {
            result = firstnum %  secondnum;
            answer = String.format("%.2f",result);
            jtxtDisplay.setText(answer);
        }
    }                                          

